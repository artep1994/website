    <div class="project_description">
    For a semester long, students of the KABK BA&MA Graphic Design departments have investigated questions around our current global climate crises through the lens of queer and decolonial theory.
Together with the students of the Rotterdam Academy of Architecture and Urban Design (RAVB), their works are exhibited at ‘NOW! Designing in Times of Climate crisis’ as the last exhibition for the International Architecture Biennale Rotterdam.
<br><br>
Based on some elements of the already existing identity of IABR: Down to Earth (designed by Roosje Klap), I have developed a visual system based on the notion of verticality, inspired by the question IABR was asking: Where can we land? The system was both in use for production of social media content and for development of the graphical content in the exhibition space.
