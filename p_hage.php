    <div class="project_description">
      Critical Mass Utrecht has organised an evening talk, taken place at the National Theater in The Hague, revolving around the topic of Dutch identity and whether is it placed under pressure? They wished to create an open space for discussion, and meaningfully engage with the audience with the help of a Buzzmaster.
  For this event, we have created a stand-alone audio-visual installation, which the visitors of the event could listen to before or after the talk.
<br>
<br>
An engaging discussion, which I have witnessed between my dutch coworkers about being a ‘Hagenees’ or ‘Hagenaar’ served as a starting point in investigating the identity of the citizens living in The Hague.

This video work tries to grasp and capture how I tried gain an understanding as a foreigner of what this difference actually was, and more importantly, what is the point using two slightly different words. Needless to say, I was blown away with the range of diverse answers and responses I’ve received. 
