    We are all Cyborgs was a livestream series produced and broadcasted by Graphic Design students of KABK in the 3rd year, developed in collaboration of interactive media design and coding class.
Students explored a framework of critical texts from various post-automated future perspectives and have been encouraged to experiment with digital media to  translate their source materials into visual form in the given format.
The identity is to frame these diverse digital performances together, filling gaps between streams, and outlining key concepts  from which each group have developed their projects from.
